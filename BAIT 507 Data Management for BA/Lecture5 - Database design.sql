--  From ER Diagrams To Tables

-- Elements of ER Diagrams
-- Entity and Entity Type
-- Entity: A specific object or event of interest in a domain. Example: Steven, Wei
-- Entity Type: An abstract concept whose instances are entities. Example: Professor

-- Attribute: A kind of fact relevant to entities of a given type. Example: Name, Date of Birth
-- Or, A kind of fact relevant to relationships of a given type.

-- Relationship and Relationship Type
-- A specific association between two or more specific entities. Example: Wei works at UBC
-- Relationship Type: An abstract concept whose instances are relationships. Example: WorkAt

-- Cardinality Constraints
-- The number of entities of one type that can or must be associated with each entity of another type in a certain type of relationship.